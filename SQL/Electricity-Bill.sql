(
	METER_NUMBER                              NUMBER(20),
 	BILL_DATE                                 DATE default sysdate,
 	CURRENT_UNITS                             NUMBER(10) default 0,
 	PREVIOUS_UNITS                            NUMBER(10) default 0,
 	CONSUMED_UNITS                            NUMBER(10) default 0,
 	AMOUNT                                    NUMBER(15) default 0,
 	DUE_DATE                                  DATE default sysdate,
 	PAID                                      VARCHAR2(20) default 'NO',
	CONSTRAINT BILL_PK PRIMARY KEY(METER_NUMBER),
	CONSTRAINT CUSTOMER_FK FOREIGN KEY(METER_NUMBER)
);

